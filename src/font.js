export const CHAR_W = 3;
export const CHAR_H = 5;

// Draw a string of glyphs. scale = pixel size per font dot. Returns width drawn.
export function drawText(ctx, str, x, y, scale, color) {
  ctx.fillStyle = color;
  let cx = x;
  const text = String(str).toUpperCase();
  for (const ch of text) {
    const glyph = G[ch] || G[" "];
    for (let row = 0; row < CHAR_H; row++) {
      const bits = glyph[row];
      for (let col = 0; col < CHAR_W; col++) {
        if (bits[col] === "1") {
          ctx.fillRect(cx + col * scale, y + row * scale, scale, scale);
        }
      }
    }
    cx += (CHAR_W + 1) * scale;
  }
  return cx - x;
}

export function textWidth(str, scale) {
  return String(str).length * (CHAR_W + 1) * scale - scale;
}

export function drawTextCentered(ctx, str, cx, y, scale, color) {
  const w = textWidth(str, scale);
  drawText(ctx, str, Math.round(cx - w / 2), y, scale, color);
}
